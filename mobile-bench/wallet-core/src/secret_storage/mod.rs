//! Multi-curve secret storage — Rust port of
//! `@midnight-ntwrk/midnight-did-secret-storage`.
//!
//! Provides typed key management across the three curves the
//! Midnight DID protocol uses:
//!
//! - **Ed25519** (kty=OKP, crv=Ed25519) — RFC 8032 EdDSA.
//! - **P-256** (kty=EC, crv=P-256) — NIST FIPS 186-4 ECDSA-SHA-256.
//! - **Jubjub** (kty=EC, crv=Jubjub) — Midnight-specific Schnorr
//!   over the Jubjub embedded curve, sharing primitives with the
//!   on-chain DID controller signing path.
//!
//! Layout mirrors the upstream package:
//!
//! - [`types`] — `PublicJwk`, `StoredKeyMeta`, `SecretStorage`
//!   trait, input structs.
//! - [`errors`] — `SecretStoreError` variants.
//! - [`crypto`] — symmetric envelope (scrypt-derived key + AES-GCM)
//!   for at-rest encryption of the file store.
//! - [`curve_support`] — per-curve key generation, signing,
//!   verification.
//! - [`hd_derivation`] — BIP32 + HKDF deterministic seed → key.
//! - [`seed`] — hex-encoded seed parsing/validation.
//! - [`file_secret_store`] — encrypted JSON file backend.
//!
//! Not API-compatible with the upstream's on-disk format: we
//! prioritise idiomatic Rust + serde over wire compatibility with
//! the JS implementation. The crypto algorithms (Ed25519, P-256,
//! AES-GCM, scrypt, HKDF) are all standards-defined, so keys
//! generated by either side are mathematically interoperable —
//! only the file envelope and JSON layout differ.

pub(crate) mod crypto;
pub(crate) mod curve_support;
pub mod errors;
pub(crate) mod hd_derivation;
pub mod types;

pub use errors::SecretStoreError;
pub use types::{
    DeriveKeyFromSeedInput, GenerateKeyInput, ImportKeyInput, MidnightCurve, MidnightKeyType,
    PublicJwk, SecretKeyRef, SecretStorage, SignOutput, StoredKeyMeta, VerifyInput,
};
